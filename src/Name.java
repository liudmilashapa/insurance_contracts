//*--------------------------------------------------------------------------------------------------------------*//

public class Name {

//*--------------------------------------------------------------------------------------------------------------*//

    private String firstName;
    private String lastName;

//*--------------------------------------------------------------------------------------------------------------*//

    Name(String firstName, String lastName){
        this.firstName = firstName;
        this.lastName = lastName;
    }

//*--------------------------------------------------------------------------------------------------------------*//

    String getFirstName(){
        return firstName;
    }

    String getLastName(){
        return lastName;
    }
}

